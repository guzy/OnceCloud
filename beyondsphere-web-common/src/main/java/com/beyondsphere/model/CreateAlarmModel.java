package com.beyondsphere.model;

import org.json.JSONArray;

public class CreateAlarmModel {
	private String alarmUuid;
	private String alarmName;
	private String alarmSameRule;
	private String alarmPeriod;
	private JSONArray alarmRules;

	public String getAlarmUuid() {
		return alarmUuid;
	}

	public void setAlarmUuid(String alarmUuid) {
		this.alarmUuid = alarmUuid;
	}

	public String getAlarmName() {
		return alarmName;
	}

	public void setAlarmName(String alarmName) {
		this.alarmName = alarmName;
	}

	public String getAlarmSameRule() {
		return alarmSameRule;
	}

	public void setAlarmSameRule(String alarmSameRule) {
		this.alarmSameRule = alarmSameRule;
	}

	public String getAlarmPeriod() {
		return alarmPeriod;
	}

	public void setAlarmPeriod(String alarmPeriod) {
		this.alarmPeriod = alarmPeriod;
	}

	public JSONArray getAlarmRules() {
		return alarmRules;
	}

	public void setAlarmRules(JSONArray alarmRules) {
		this.alarmRules = alarmRules;
	}

}
