package com.beyondsphere.model;

public class CreateBackModel {
	private String name;
	private String backUuid;
	private String vmUuid;
	private String vmIP;
	private int port;
	private int weight;
	private String feUuid;
	private String lbUuid;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBackUuid() {
		return backUuid;
	}
	public void setBackUuid(String backUuid) {
		this.backUuid = backUuid;
	}
	public String getVmUuid() {
		return vmUuid;
	}
	public void setVmUuid(String vmUuid) {
		this.vmUuid = vmUuid;
	}
	public String getVmIP() {
		return vmIP;
	}
	public void setVmIP(String vmIP) {
		this.vmIP = vmIP;
	}
	public int getPort() {
		return port;
	}
	public void setPort(int port) {
		this.port = port;
	}
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}
	public String getFeUuid() {
		return feUuid;
	}
	public void setFeUuid(String feUuid) {
		this.feUuid = feUuid;
	}
	public String getLbUuid() {
		return lbUuid;
	}
	public void setLbUuid(String lbUuid) {
		this.lbUuid = lbUuid;
	}
	
	
}
